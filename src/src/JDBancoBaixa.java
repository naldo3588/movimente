    private void carregarcombobox() {
        try {
            String nome_banco = jComboBoxBanco.getSelectedItem().toString();
            iniciarBD();
            rsListar = stmtListar.executeQuery("SELECT * FROM cad_banco where banco='" + nome_banco + "' ORDER BY id_banco DESC ");
            while (rsListar.next()) {
                jComboBoxFavorecido.addItem(rsListar.getString(2));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro " + e.getMessage());
        }
    }

    private void carregarTF() {

        iniciarBD();
        try {
            rsListar = stmtListar.executeQuery("SELECT * FROM cad_banco where nome_favorecido like '%" + jComboBoxFavorecido.getSelectedItem().toString() + "%' and banco='" + jComboBoxBanco.getSelectedItem().toString() + "'");
            if (rsListar.next()) {
                jTextFieldAG.setText(rsListar.getString("agencia"));
                jTextFieldCC.setText(rsListar.getString("conta"));
                jTextFieldID_banco.setText(rsListar.getString("id_banco"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDBancoBaixa.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void iniciarBD() {
        try {
            con = ConexaoFactory.getConnection();
            stmtListar = con.createStatement();

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error " + ex.getMessage());
        }
    }

    private void limparcampos() {

        jTextFieldAG.setText("");
        jTextFieldCC.setText("");
        jComboBoxBanco.setSelectedItem("Selecione");
    }

    public void salvar() {

        nome_favorecido = jComboBoxFavorecido.getSelectedItem().toString();
        id_banco = Integer.parseInt(jTextFieldID_banco.getText());
        nome_banco = jComboBoxBanco.getSelectedItem().toString();
        int option = JOptionPane.showConfirmDialog(rootPane, "Confirma?");
        if (option == JOptionPane.YES_OPTION) {
            this.dispose();
        } else {

        }
    }

