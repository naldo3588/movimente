    private void montarTabela() {
        int linha = 0;
//         jTableParcelas.setDefaultRenderer(Double.class,);
        try {
            while (rsListar.next()) {
                int numero_parcela_contrato = rsListar.getInt(1);
                int id_contrato = rsListar.getInt(2);
                int numero_parcelas = rsListar.getInt(3);
                String tipo_pgto = rsListar.getString("tipo_pagamento");
                Double valor = rsListar.getDouble(4);
                Date data_vecimento = rsListar.getDate("data_vencimento");
                Date data_baixa = rsListar.getDate("data_baixa");
                String usuario = rsListar.getString("usuario");

                jTableParcelas.getModel().setValueAt(numero_parcela_contrato, linha, 0);
                jTableParcelas.getModel().setValueAt(id_contrato, linha, 1);
                jTableParcelas.getModel().setValueAt(numero_parcelas, linha, 2);
                jTableParcelas.getModel().setValueAt(tipo_pgto, linha, 3);
                jTableParcelas.getModel().setValueAt(valor, linha, 4);
                jTableParcelas.getModel().setValueAt(data_vecimento, linha, 5);
                jTableParcelas.getModel().setValueAt(data_baixa, linha, 6);
                jTableParcelas.getModel().setValueAt(usuario, linha, 7);

                if (data_baixa == null) {
                    jTableParcelas.getModel().setValueAt("Em Aberto", linha, 6);
                }

                linha++;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error " + ex.getMessage());
        }
    }

