    private void limparcampos() {
        jTextFieldNomeFavorecido.setText("");
        jTextFieldAgencia.setText("");
        jTextFieldContaCorrente.setText("");
        jComboBoxBanco.setSelectedItem("Selecione");
    }

    private boolean validarcamposvazios() {

        if (jTextFieldNomeFavorecido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite um nome!", "Digite um nome", WIDTH);
            jTextFieldNomeFavorecido.requestFocus();
        } else if (jTextFieldAgencia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite a Cidade!", "Digite a Cidade", WIDTH);
            jTextFieldContaCorrente.requestFocus();
        } else if (jTextFieldContaCorrente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite O Endereço!", "Digite o Endereço", WIDTH);
            jTextFieldContaCorrente.requestFocus();

        } else {
//            JOptionPane.showMessageDialog(null, " Funcionario " + jTextFieldNovoNome.getText() + " atualizado");
            return false;
        }

        return true;
    }

    public void salvar() {
        if (validarcamposvazios() == false) {

            BancoBean banco = new BancoBean();
            BancoDAO dao = new BancoDAO();
            try {
                banco.setNome_favorecido(jTextFieldNomeFavorecido.getText());
                banco.setAgencia(jTextFieldAgencia.getText());
                banco.setConta(jTextFieldContaCorrente.getText());
                banco.setNome_banco(jComboBoxBanco.getSelectedItem().toString());
                dao.inserir(banco);
                limparcampos();
                jTextFieldNomeFavorecido.requestFocus();

            } catch (Exception e) {
            }

        }
    }

