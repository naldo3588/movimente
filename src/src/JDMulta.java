    private void buscaMulta() {
        try {
            con = ConexaoFactory.getConnection();
            stmtListar = con.createStatement();
            rsListar = stmtListar.executeQuery("SELECT valor_multa FROM multa");
            if (rsListar.next()) {
                jTextFieldValorAtual.setText(rsListar.getString("valor_multa"));
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error " + ex.getMessage());
        }
    }

    private void Salvar() {
        if (jFormattedTextFieldNovoValor.getText().contains("-")) {
            JOptionPane.showMessageDialog(this, "Digite um Valor valido !!");
        } else if ((jFormattedTextFieldNovoValor.getText().equals("0"))) {
            JOptionPane.showMessageDialog(this, "Digite um Valor valido !!");
        } else if ((!jFormattedTextFieldNovoValor.getText().contains("."))) {
            JOptionPane.showMessageDialog(this, "Digite um Valor valido !!");
        } else {

            try {
                con = ConexaoFactory.getConnection();
                stmtListar = con.createStatement();
                stmtListar.executeUpdate("UPDATE multa SET valor_multa ='" + jFormattedTextFieldNovoValor.getText() + "'");
                JOptionPane.showMessageDialog(null, "Multa Atualizada com Sucesso !!");
                jFormattedTextFieldNovoValor.requestFocus();
                jFormattedTextFieldNovoValor.setText("");
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error " + ex.getMessage());
            }
        }
    }

    public void Incluir() {

        ParcelasBean parcela = new ParcelasBean();
        ParcelasDAO dao = new ParcelasDAO();

        try {

            parcela.setMulta(Double.parseDouble(jFormattedTextFieldNovoValor.getText()));
            dao.inserirMulta(parcela);

        } catch (NumberFormatException e) {
        }

    }

